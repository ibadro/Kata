package Java0.Условные_операторы_и_циклы;

public class Switch1 {
   // static boolean bool = true;
    static int a = 8;

    public static void main(String[] args) {
        switch (a){
            case (5):
                System.out.println("5");
                break;
            case (6):
                System.out.println("6");
                break;
            case 8:
                System.out.println("8");
                break;
        }
    }
}
